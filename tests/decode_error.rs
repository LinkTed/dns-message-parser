use bytes::Bytes;

use dns_message_parser::Dns;

fn decode_msg_error(msg: &[u8]) {
    // Decode BytesMut to message
    let bytes = Bytes::copy_from_slice(&msg[..]);
    // Decode the DNS message
    let dns = Dns::decode(&bytes);
    // Check the result
    assert!(dns.is_err())
}

